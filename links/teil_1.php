                            <p>Ein anonymes Dokument in einer spanischen 
                                Bibliothek hatte mich 1985 erstmals auf die Spur eines anderen, heute 
                                weitgehend unbekannten Christoph Kolumbus gebracht. Es handelte sich um 
                                ein &auml;lteres Vortragsmanuskript, in welchem die Chronisten des 15. 
                                Jahrhunderts zitiert und mit den entsprechenden Aussagen des Kolumbussohnes 
                                Hernando Col&oacute;n verglichen wurden. In der Folge suchte ich in Archiven 
                                und Bibliotheken nach weiteren &#8222;Puzzlest&uuml;cken&#8220;, sichtete 
                                Quellen und begann, mich kritisch mit der Geschichtsschreibung des 19. 
                                und 20. Jahrhunderts zu besch&auml;ftigen. </p>
                                
