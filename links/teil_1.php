                                <h4>&#8222;Polemiken&#8220; rund um das Jahr 1992</h4>
                            </div>
                            <div class="col-md-6">
                                <p>
                                  
                                Der spanische Historiker und Kolumbusforscher Antonio Rumeu 
                                de Armas warnte im Jahre 1991 entschieden vor einer Besch&auml;ftigung mit 
                                den fr&uuml;hen Lebensjahren des Kolumbus und forderte mit Nachdruck, dass 
                                diese als &quot;unl&ouml;sbares R&auml;tsel&quot; in die Geschichte eingehen 
                                m&uuml;ssten. Aus gewissen Quellen erg&auml;ben sich n&auml;mlich zwangsl&auml;ufig 
                                &#8222;Polemiken&#8220; (Rumeu 1991, S. 17). <br>
                                Wie kommt es, dass Aussagen von Chronisten bis in die Gegenwart als bedrohlich 
                                empfunden werden? Und um welche Art von Polemiken k&ouml;nnte es sich handeln?<br>
                                <p>Rumeu de Armas&#8217; Haltung wird verst&auml;ndlich, 
                                wenn man den politischen Hintergrund in Spanien vor der 500-Jahr-Feier 
                                der Entdeckung Amerikas (1992) betrachtet. Damals war eine gem&auml;ssigte 
                                Darstellung angebracht, nicht zuletzt aus R&uuml;cksicht auf die in Madrid 
                                lebenden Nachfahren des ber&uuml;hmten Entdeckers. Die Familie Col&oacute;n 
                                hatte im 20. Jahrhundert bereits zwei Vertreter mit dem symboltr&auml;chtigen 
                                Namen Crist&oacute;bal Col&oacute;n durch Attentate verloren: den ersten 
                                1936 im Spanischen B&uuml;rgerkrieg, den zweiten erst im Februar 1986, 
                                w&auml;hrend eines Nato-Gipfeltreffens in Madrid. Letzterer war Vizeadmiral 
                                der spanischen Flotte und (Titular-)Herzog von Veragua (Panama); kurz 
                                vor dem Anschlag hatte er den Vorsitz im Komitee zur Vorbereitung der 
                                500-Jahr-Feier &uuml;bernommen (Col&oacute;n de Carvajal 1992, S. 153-56). 
                                Das Bem&uuml;hen des Historikers, das Kolumbusbild in der Folge moderat 
                                zu halten und alle negativ auslegbaren Details in den Quellen aus seinem 
                                Leben auszublenden, ist unter dem Eindruck dieses Mordanschlags verst&auml;ndlich. 
                                Des Attentats von 1986 wurde &uuml;brigens die baskische Untergrundorganisation 
                                ETA verd&auml;chtigt; angesichts der Umst&auml;nde kommt aber auch der 
                                Geheimdienst Panamas in Frage; der damalige Staatschef Manuel Noriega 
                                hatte sich mit der spanischen Aristokratie in Panama angelegt, stand w&auml;hrend 
                                der Iran-Contra-Aff&auml;re im Sold der USA und pflegte Kontakte mit Kuba 
                                und der kolumbianischen Guerilla M-19 (F. Kempe, Aufstieg und Fall Noriegas, 
                                S. 176, 192, 206). </p>
