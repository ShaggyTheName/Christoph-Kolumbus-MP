                            <h4>Negativbilder heute und fr&uuml;her</h4>
                            </div>
                            <div class="col-md-6">
                            
                                    <p>Wie erw&auml;hnt, wurde Christoph Kolumbus 
                                    und seine Entdeckung Amerikas im Laufe der Zeit h&ouml;chst unterschiedlich 
                                    eingesch&auml;tzt &#8211; je nach Zeitgeist und vorherrschender Ideologie. 
                                    Jahrhunderte lang zum unantastbaren Helden emporstilisiert, kam es 1992 
                                    zu einer radikalen Trendwende: Er trage, so kritisierten US-Historiker, 
                                    an der Vernichtung der Ureinwohner Schuld und habe den Genozid von Millionen 
                                    von Indios zu verantworten. <br>
                                    Jahrhunderte alte Bilder &uuml;ber das brutale Vorgehen der spanischen 
                                    Konquistadoren spielten dabei eine entscheidende Rolle. Diese gehen auf 
                                    den Dominikaner <a href="http://de.wikipedia.org/wiki/Bartolom%C3%A9_de_Las_Casas" target="_blank">Bartolom&eacute; 
                                    de Las Casas</a> (ca. 1474&#8211;1566) zur&uuml;ck, der in seinem &#8222;Kurzbericht 
                                    &uuml;ber die Verw&uuml;stung Westindiens&#8220; (Br&eacute;vissima relaci&oacute;n 
                                    de la destrucci&oacute;n de las Indias occidentales) die Spanier als &#8222;Tyrannen&#8220;, 
                                    &#8222;grausamste W&ouml;lfe&#8220; sowie als &#8222;Tiger und L&ouml;wen&#8220; 
                                    bezeichnet, die &#8222;unz&auml;hlige Grausamkeiten&#8220; gegen&uuml;ber 
                                    den Indios ver&uuml;bt hatten. Sein Kurzbericht erschien 1552 im Druck 
                                    und wurde sp&auml;ter in den besetzten Niederlanden und in den protestantischen 
                                    Gebieten Europas zu einem wirksamen Propagandainstrument gegen das expandierende 
                                    &#8222;Reich der nie untergehenden Sonne&#8220; <a href="http://de.wikipedia.org/wiki/Philipp_II._(Spanien)" target="_blank">Philipps 
                                    II.</a> und seiner Nachfolger. Spanische Historiker sprechen noch heute 
                                    von einer schwarzen Legende (leyenda negra), ausgel&ouml;st durch den 
                                    Dominikaner.<br>
                                    <br>
                                  </p>
                              
                                  <p>Im Jahre 2006, als sich der Todestag des Christoph 
                                  Kolumbus zum 500. Mal j&auml;hrte, taten sich Journalisten mit einer n&uuml;chternen 
                                  Einsch&auml;tzung des Entdeckers schwer. Sie fragten nach, ob Kolumbus nun 
                                  &#8222;ein B&ouml;ser&#8220; gewesen sei, einer, der Indianer abgeschlachtet 
                                  und versklavt habe, oder &#8222;ein Guter&#8220;, einer, der diesen eher 
                                  wohlgesinnt gewesen sei. <a href="http://de.wikipedia.org/wiki/Bartolom%C3%A9_de_Las_Casas" target="_blank">Bartolom&eacute; 
                                  de Las Casas</a> selbst hatte in Bezug auf Kolumbus kaum Negatives zu berichten. 
                                  Obwohl der M&ouml;nch die Eroberung (Conquista) von allem Anfang an grundlegend 
                                  kritisierte, beschrieb er den Genueser Entdecker als den integersten aller 
                                  Gouverneure, ja er bezeichnete ihn sogar als einen Propheten, der von Gott 
                                  f&uuml;r die Regierung in &Uuml;bersee auserw&auml;hlt gewesen sei! Las 
                                  Casas, dessen Vater bereits mit Kolumbus gereist war, kopierte sp&auml;ter 
                                  das Bordbuch Kolumbus&#8217; zur ersten Reise. Dabei sah ersich durch die 
                                  Aussagen des &#8222;alten Admirals&#8220; in seiner eigenen, positiven Stilisierung 
                                  der Indios best&auml;tigt; Kolumbus glaubte, dass es sich bei den Einheimischen 
                                  um die Untertanen des Grossen Khans handele, der die Ostchristen einst wohlwollend 
                                  in sein Reich aufgenommen hatte. 
                              
                               
                                  
                               In den Reiseberichten des Genueser Seefahrers ist auch ein 
                                  grosses Misstrauen des Kolumbus gegen&uuml;ber seinen Mitreisenden sp&uuml;rbar. 
                                  Sie waren die Untertanen des katholischen <a href="http://de.wikipedia.org/wiki/Ferdinand_II._von_Arag%C3%B3n" target="_blank">K&ouml;nigs 
                                  Ferdinands II. von Aragon</a> (1452&#8211;1516), den Niccol&ograve; Machiavelli 
                                  (1469&#8211;1527) wegen seiner Skrupellosigkeit zum Vorbild f&uuml;r seinen 
                                  &#8222;il principe&#8220; genommen hatte. Vor allem hatte Kolumbus gem&auml;ss 
                                  der &#8222;Kolumbinischen Tradition&#8220; einst im Krieg gegen seinen sp&auml;teren 
                                  Auftraggeber gestanden.</p>
