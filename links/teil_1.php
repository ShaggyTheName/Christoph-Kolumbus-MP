                            <h4>Der Besitz des Paradieses</h4>
                            </div>
                            <div class="col-md-6">
                            
                            <p> Im Jahre 1493, nach Kolumbus&#8217; R&uuml;ckkehr 
                                  von der ersten Entdeckungsfahrt, kam es zu einer unvorstellbaren Aufbruchstimmung 
                                  in Europa. Berichte vom mildem Klima, der &uuml;ppigen Vegetation und den 
                                  friedfertigen Einwohnern befl&uuml;gelten die Phantasie der von Krieg und 
                                  Misswirtschaft gebeutelten Zeitgenossen. Kolumbus war seit seiner dritten 
                                  Reise davon &uuml;berzeugt, das irdische Paradies entdeckt zu haben &#8211; 
                                  gem&auml;ss mittelalterlicher Weltauffassung musste es sich im &auml;ussersten 
                                  Osten befinden. Der Genueser Seefahrer sprach &uuml;berdies von den sagenhaften 
                                  Goldsch&auml;tzen des biblischen K&ouml;nigs Salomo und erinnerte daran, 
                                  dass gem&auml;ss der &Uuml;berlieferung im irdischen Paradies Enoch und 
                                  Elias darauf warteten, befreit zu werden; angesichts der Endzeit sollten 
                                  diese sich (bald) im Kampf gegen den Antichristen bew&auml;hren. Kein Zweifel: 
                                  Der ausl&auml;ndische Seefahrer verlieh seiner Entdeckungsfahrt den Stellenwert 
                                  einer neuen Offenbarung. Wer aber war dazu auserw&auml;hlt, in &Uuml;bersee 
                                  zu herrschen, wem sollte dieses verheissungsvolle Gebiet geh&ouml;ren? <br>
                                  Las Casas bezog in dieser Frage Stellung: Er verwies auf den Kronvertrag 
                                  von Santa F&eacute; (1492), in welchem die weitreichenden Privilegien f&uuml;r 
                                  Kolumbus und seine Nachkommen festgehalten waren: die Gouverneursrechte, 
                                  die Admirals- und Vizek&ouml;nigstitel sowie das Recht auf finanzielle Beteiligung 
                                  der Familie Col&oacute;n.<br>
                            
                                 
                               Durch seine Parteinahme f&uuml;r Kolumbus und seine Erben stellte Las 
                                  Casas aber das rigorose Vorgehen von Papst <a href="http://de.wikipedia.org/wiki/Alexander_VI." target="_blank">Alexander 
                                  VI.</a> (Roderigo Borgia) in Frage. Dieser hatte den Kronvertrag von Santa 
                                  F&eacute; (1492) f&uuml;r ung&uuml;ltig erkl&auml;rt und das neue, unbekannte 
                                  Gebiet bereits 1493 seinem Landsmann Ferdinand von Aragon als Schenkung 
                                  zukommen lassen. Alexander Borgia begr&uuml;ndete dieses Vorgehen mit seiner 
                                  eigenen, uneingeschr&auml;nkten Macht: Er berief sich auf imperiale Schriften 
                                  aus der Kreuzzugszeit, die den Papst zur obersten (Welt-) Instanz erhoben 
                                  hatten. 
                             
                               Nach dem Tod Ferdinands von Aragon fand Las Casas Geh&ouml;r 
                                  bei Adrian von Utrecht, dem sp&auml;teren Reformpapst <a href="http://de.wikipedia.org/wiki/Hadrian_VI." target="_blank">Hadrian 
                                  VI</a>. (1522&#8212;23). Zudem stand er, wie bereits Kolumbus zuvor, unter 
                                  dem Einfluss von <a href="http://bautz.de/bbkl/x/ximenes_f.shtml" target="_blank">Jim&eacute;nez 
                                  de Cisneros</a> (siehe: <a href="http://www.christopher-columbus.ch/teil_6.html" target="_blank">Kreuzzugsmentalit&auml;t</a>). 
                                  Dieser m&auml;chtige franziskanische Reformkardinal ernannte ihn 1517 zum 
                                  Schutzbeauftragten bzw. zum &#8222;Anwalt der Indios&#8220; (procurador), 
                                  der f&uuml;r die Umsetzung seiner utopischen Projekte im vermeintlichen 
                                  Indien (las Indias) ein j&auml;hrliches Honorar erhielt.</p>
                               
                                 
                             
                    
                                <p>Auch der Habsburger <a href="http://de.wikipedia.org/wiki/Karl_V._(HRR)" target="_blank">Kaiser 
                                    Karl V.</a> unterst&uuml;tzte Bartolom&eacute; de Las Casas anf&auml;nglich 
                                    &#8211; er war es, der ihm 1542 den Auftrag erteilte, den oben erw&auml;hnten, 
                                    folgenschweren Kurzbericht &uuml;ber die Missst&auml;nde in Westindien 
                                    zu verfassen (&#8230;). Der junge Monarch musste sich nach seinem Regierungsantritt 
                                    nicht nur auf der Iberischen Halbinsel, sondern auch in &Uuml;bersee durchsetzen. 
                                    Ein vehementer Kritiker der dortigen Zust&auml;nde konnte ihm dabei nur 
                                    hilfreich sein.<br>
                                    </p>
                                 
