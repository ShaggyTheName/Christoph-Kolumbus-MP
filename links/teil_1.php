                                den Nationalismus bis hin zur Gegenwart &#8211; die Biographie des Kolumbus 
                                bestimmt hatten. Im Laufe der historischen Rezeption waren verschiedene 
                                Aspekte, unter anderem die fr&uuml;heren Lebensjahre des Kolumbus, tabuisiert 
                                worden: Zu chaotisch erschienen der Nachwelt die sp&auml;tmittelalterlichen 
                                Kriegswirren im damaligen Europa, zu abwegig und zu bizarr aber auch die 
                                ureigensten religi&ouml;sen Vorstellungen des Genueser Seefahrers.</p>
