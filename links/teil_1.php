                            <div class="col-md-12">
                            <h4>Die Tabuisierung der &Uuml;berlieferung </h4>
                            </div>
                            <div class="col-md-6">
                            
                            <p><a href="http://de.wikipedia.org/wiki/Bartolom%C3%A9_de_Las_Casas" target="_blank">Bartolom&eacute; 
                                  de Las Casas</a> hatte Spanien willentlich oder unwillentlich einen riesigen 
                                  Imageschaden zugef&uuml;gt. Deshalb ist es nachvollziehbar, dass sein Hauptwerk, 
                                  die umfangreiche &#8222;Geschichte der Indien&#8220; (Historia de las Indias) 
                                  Jahrhunderte lang unter Verschluss blieb. Erst auf Druck der sich abl&ouml;senden 
                                  Kolonie Kuba kam es in Spanien 1875 zu einer Erstausgabe. Auch die Chronik 
                                  des Kolumbus-Sohnes Hernando Col&oacute;n blieb anf&auml;nglich unediert. 
                                  Hernando hatte seinen Bericht &uuml;ber das Leben und die Taten seines Vaters 
                                  kurz vor seinem Tod 1539 vollendet. Das Manuskript wurde 1571 als kleinformatige, 
                                  italienische &Uuml;bersetzung in Venedig ver&ouml;ffentlicht (siehe: <a href="http://www.christopher-columbus.ch/teil_7.html" target="_blank">Hernando 
                                  Col&oacute;n</a>).</p>
                                 
                            </div>

                            <div class="col-md-6">
                            <p>Zur Zeit der Aufkl&auml;rung und in den folgenden Jahren 
                                  der Franz&ouml;sischen Revolution r&uuml;hmten renommierte Historiker die 
                                  korrekte und transparente Arbeitsweise des humanistisch gebildeten Kolumbus-Sohnes. 
                                  Diese Beurteilung blieb bis in die zweite H&auml;lfte des 19. Jahrhunderts 
                                  bestehen. Danach wurde die Chronik zerpfl&uuml;ckt und Textstellen, die 
                                  dem aufkommenden spanischen oder italienischen Nationalbewusstsein widersprachen, 
                                  aus der Geschichtsschreibung ausgeschieden. So beispielsweise auch die Tatsache, 
                                  dass die Hafenstadt Genua ihrem erfolgreichen Sohn zeitlebens die von ihm 
                                  erhoffte Unterst&uuml;tzung versagte. <br></p>
