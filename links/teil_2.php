                    <h4>Einwanderer aus Savoyen-Burgund?</h4>
                    </div>
                    <div class="col-md-6">
                    
                    <p> Kolumbus hatte einen grossen Teil seiner Kindheit 
      ausserhalb des Genueser Stadttores des heiligen Andreas (extra portam Sanct&#8217; 
      Andreae) verbracht. Dort besass sein Vater Domenico Colombo ein Anwesen 
      mit Haus, Laden, Brunnen und Garten. Im frühen 14. Jahrhundert waren 
      am selben Ort zum ersten Mal zwei Colombos registriert worden (Desimoni 
      1894), und es stellt sich die Frage, woher diese gekommen sein m&ouml;gen. 
      Betrachtet man die ältesten Familien mit dem Wortstamm Colomb, so finden 
      sich diese in der an Genua angrenzenden Grafschaft Savoyen. K&ouml;nnte 
      die Familie des Entdeckers, die den Beinamen &#8222;vom Roten Land&#8220; 
      (de terra Rubra) trug, ursprünglich aus Savoyen stammen? Auszuschliessen 
      ist dies nicht, denn wenige Jahre vor dem Auftauchen der ersten Colombos 
      beim Stadttor des heiligen Andreas (1311 und 1329), wo Kolumbus seine Kindheit 
      verbrachte, war eines dieser alten Geschlechter in Savoyen &#8222;erloschen&#8220;.
                    
      Es handelt sich um die Familie Collombey, die bis 1304 die 
      Wegrechte entlang der Alpenstrasse nach Italien besass. Die Vornamen der 
      letzten Vertreter dieses Geschlechts stimmen mit den Registrierten vor den 
      Toren Genuas überein.<br>   
      
      Die Collombeys aus dem Wallis hatten ein Siegelwappen, 
      das nicht wie üblich aus einer Taube, sondern aus einem Adler bestand. 
      Es gleicht dem Emblem eines r&ouml;mischen Unteroffiziers (aquilifer) und 
      erinnert an Hernando Col&oacute;ns Aussage, dass der Stammvater der Familie 
      Colombo einst in r&ouml;mischer Zeit mit Adler-Würden ausgezeichnet 
      worden sei.

      Dieses Geschlecht kontrollierte die Wegstrecke von der Rhonemündung 
      am Genfersee bis nach Saint Maurice. Ein erster Vertreter ist 1131 erwähnt, 
      als Henry I. von Troyes, seines Zeichens Graf von der Champagne, das Wallis 
      kolonisierte. 1304 verkauften die Adligen von Collombey ihre Wegrechte an 
      den Grafen von Savoyen; die Savoyer führten ab 1416 den Herzogstitel 
      und stiegen später zur K&ouml;nigsdynastie Italiens auf. Die Veräusserung 
      des lukrativen Wegerechts fand drei Jahre vor den Verhaftungen und Hinrichtungen 
      der Tempelritter statt und es stellt sich die Frage, ob der Verkauf nicht 
      unter Druck erfolgte. Der Nord-Süd-Handel war lukrativ, dies nicht 
      zuletzt im Woll-Fernhandel, in welchem auch Kolumbus&#8217; Vater Domenico 
      in Genua tätig war.
                       
                         
