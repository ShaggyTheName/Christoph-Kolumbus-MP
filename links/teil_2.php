                    <h4>Mitglied des Templerordens?</h4>
                    </div>
                    <div class="col-md-6">
                    
                    <p>Las Casas schrieb, dass Kolumbus einem ihm unbekannten 
                    Orden angeh&ouml;rt habe und Hernando Col&oacute;n weist in diesem Zusammenhang 
                    auf eine Abstammung vom heiligen Blut Jerusalems hin (del regal sangue di 
                    Gierusalme). Hatte der Entdecker Amerikas dem damals längst verbotenen 
                    Templerorden angeh&ouml;rt? Kolumbus hatte in Portugal tatsächlich 
                    in den engeren Zirkel des portugiesischen Christusordens eingeheiratet, 
                    der offiziellen Nachfolgeorganisation des 1307 aufgel&ouml;sten und seither 
                    verfolgten Templerordens; Kolumbus&#8217; Schwiegervater, Bartholomeu Perestrello, 
                    war ein Ritter (cavalleiro) aus dem Hause <a href="http://de.wikipedia.org/wiki/Heinrich_der_Seefahrer" target="_blank">Heinrichs 
                    des Seefahrers</a> (1394&#8211;1406) gewesen, des legendären Grossmeisters 
                    dieses Ordens.
                
                    Christoph Kolumbus unterzeichnete all seine Briefe mit einem pyramidenförmigen Kryptogramm: .S. .S. A. S. (siehe Abbildung). Gemäss dem Römer Valerius Probus entsprachen Abkürzungen wie .S. A. S. einer Grussformel an eine Ortschaft oder an ein Heiligtum wie zum Beispiel: Salve Aquileium Sacrum (Probus, ed. 1494). Die oben genannte Ortschaft Saint Maurice heisst lateinisch Acaunum bzw. Agaunum und war im Mittelalter eine weit herum bekannte Märtyrerstätte (der Legende zufolge starb der heilige Mauritius mit seiner christlich-ägyptischen Legion bei Agaunum, im Grenzgebiet zwischen Gallia und Italia, um 302 n. Chr. den Märtyrertod.
                    K&ouml;nnte das Kryptogramm des Entdeckers ein Kürzel 
                    für Salve Agaunum Sacrum sein? Wir k&ouml;nnen es nicht wissen, doch 
                    im Vergleich zu anderen, weithergeholten Auslegungen von Kolumbus&#8217; 
                    Unterschrift erscheint dies plausibel: Die Anjou-Dynastie gründete 
                    1434 in Ripaille am Genfersee zu Ehren des Märtyrers den Ritterorden 
                    des heiligen Mauritius, und die Colombos zu Genua zählten lange Zeit 
                    zur zudienenden Schicht eben dieser Dynastie (<a href="teil_3.html">siehe 
                    Teil 3</a>).
                    </p>
                         
