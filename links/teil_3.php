                        <h4>Eine Reise in die Levante</h4>
                    </div>
                    <div class="col-md-6">
                    
                    <p>Gemäss einer Bordbuchnotiz hielt sich Kolumbus zu unbekanntem Zeitpunkt auf der Insel Chios im östlichen Mittelmeer auf. Hier hatte er gemäss eigenen Angaben beobachten können, welch grosse Gewinne die Ernten des Mastix-Harzes einbrachten. Das Harz des Mastix-Baumes diente im Mittelalter der Weihrauch- und Heilmittelherstellung; die Maonen, die genuesischen Kolonialgesellschaften, kontrollierten auf Chios den Markt dieses begehrten Rohstoffes.
Kolumbus betont, dass der Aufenthalt in der Levante ihn in erster Linie inspiriert habe: Durch Gespräche mit gelehrten Griechen, Juden und Mauren sei er zum Studium der Kosmographie angeregt worden. Von nun an habe ihn die Neugier gepackt, die Welt zu erkunden.</p>
                        
