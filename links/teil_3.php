                    <h4>Schiffbruch bei Sagres 1476</h4>
                    </div>
                    <div class="col-md-6">
                    
                    <p>Im Frühsommer 1476 zogen die Engländer aus 
                    der Normandie ab und der franz&ouml;sische Vizeadmiral und Columbus der 
                    Jüngere brachen aus der Normandie in Richtung Iberische Halbinsel 
                    auf. Dort war im Vorjahr der Erbfolgekrieg um Kastilien ausgebrochen und 
                    der franz&ouml;sische K&ouml;nig hatte Portugal militärische Unterstützung 
                    versprochen. <br>
                    Der Krieg um Kastilien war allerdings bereits entschieden: K&ouml;nig 
                    Ferdinand von Aragon hatte <a href="http://de.wikipedia.org/wiki/Alfons_V._(Portugal)" target="_blank">Alfons 
                    V. von Portugal </a>bei beim Städtchen Toro am Duero 1475 vernichtend 
                    geschlagen. Den franz&ouml;sischen Korsaren Columbus verblieb nun der 
                    Auftrag, den besiegten portugiesischen K&ouml;nig Alfons V. mit einem 
                    Teil seiner Flotte und seines Hofstabes von Lissabon nach Frankreich zu 
                    eskortieren. Dies war keine leichte Aufgabe, denn der Weg führte 
                    durch die von Ferdinand von Aragon kontrollierte Meeresenge von Gibraltar. <br>
                    Als der franz&ouml;sisch-portugiesische Konvoi 
                    im Sommer 1476 den südwestlichen Zipfel Portugals erreichte, stellten 
                    sich ihm beim Kap von San Vicente gegnerische Schiffe entgegen. Diese er&ouml;ffneten 
                    das Feuer und es kam zu einer stundenlangen Seeschlacht mit Hunderten von 
                    Toten. Gemäss Hernando Col&oacute;n kämpfte Christoph Kolumbus 
                    auf franz&ouml;sischer Seite mit. Er sei mit einer grossen Galeasse verhakt 
                    gewesen, als sein Schiff Feuer fing und er sich gezwungen sah, an die Küste 
                    zu schwimmen. Kolumbus selbst bezeichnet die Seeschlacht später als 
                    den Ursprung seiner &#8222;wundersamen Ankunft&#8220; in Portugal. Was er 
                    nicht erwähnt, ist dass er in dieser Seeschlacht gegen die eigenen 
                    Landsleute kämpfte. Die grosse Galeasse geh&ouml;rte nämlich Herzog 
                    <a href="http://de.wikipedia.org/wiki/Karl_der_K%C3%BChne" target="_blank">Karl 
                    dem Kühnen</a> von Burgund und die Mannschaft bestand unerwarteterweise 
                    aus der Republik Genua; einzelne Begleitschiffe befanden sich im Besitz 
                    der Genueser Kaufleute Spinola und Di Negro.
                    </p>
                         
