                    <h4>Georg Paläolog alias Columbus der Jüngere</h4>
                    </div>
                    <div class="col-md-6">
                    
                    <p>Ab 1475 stiess ein weiterer Flottenkommandant 
      zur Columbus-Garde: Georg Paläolog, der auch Columbus der Jüngere 
      genannt wurde. Der franz&ouml;sische K&ouml;nig <a href="http://de.wikipedia.org/wiki/Ludwig_XI._(Frankreich)" target="_blank">Louis 
      XI.</a> hatte ihn 1473 in Haft nehmen lassen, um ihn während zwei Jahren 
      zum Kommandanten der franz&ouml;sischen Flotte auszubilden (Ronci&egrave;re 
      1914, S. 377). Danach ernannte er ihn zum Marineoffizier der grossen franz&ouml;sischen 
      Gironde-Flotte. Gemeinsam mit dem oben erwähnten Vizeadmiral Guillaume 
      de Casenove kämpfte er in den <a href="http://de.wikipedia.org/wiki/Rosenkriege" target="_blank">Rosenkriegen</a> 
      gegen den englischen K&ouml;nig Eduard von York und half mit, den neunjährigen 
      Frieden von Picquigny (1475&#8212;1484) herbeizuführen. Hernando Col&oacute;n 
      schreibt, dass Christoph Kolumbus später &#8222;lange Zeit in der Gesellschaft 
      Columbus' des Jüngeren gesegelt&#8220; sei. Dieser sei &#8218;der Jüngere&#8217; 
      genannt worden, um ihn von seinem berühmt-berüchtigten älteren 
      Namensvetter zu unterscheiden (Guillaume de Casenove). <br>
      Georg Paläolog, der auch die Beinamen &#8222;Bissipat&#8220; 
      oder &#8222;der Grieche&#8220; trug, entsprang der byzantinischen Herrscherdynastie 
      der <a href="http://de.wikipedia.org/wiki/Palaiologen" target="_blank">Paläologen</a> 
      (seit 1453 aus Konstantinopel vertrieben). Da die Nachkommen des letzten 
      byzantinischen Kaisers bekannt sind, stammte er wahrscheinlich aus derjenigen 
      Seitenlinie, der die Grafschaft Montferrat n&ouml;rdlich von Genua geh&ouml;rte. 
      Columbus der Jüngere wird in den venezianischen Geheimakten oft zusammen 
      mit &#8222;complices, colligati&#8220; und &#8222;servidori&#8220; erwähnt; 
      ab und zu ist auch von einem Nicol&ograve; oder Giovanni Griego die Rede 
      - zwei weitere Byzantiner, die damals im Dienste Frankreichs standen (Salvagnini, 
      1894, S. 176).
                    </p>
                         
