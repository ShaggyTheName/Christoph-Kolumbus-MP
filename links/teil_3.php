                        <h4>Jacques Coeur</h4>
                    </div>

                    <div class="col-md-6">
                    
                        <p> <a href="http://de.wikipedia.org/wiki/Jacques_Coeur" target="_blank">Jacques 
                        Coeur</a> (ca. 1395&#8212;1456) hatte Genua im Jahre 1447 mit Hilfe der 
                        ligurischen Familien Fieschi und deren Mitstreiter &#8211; darunter Kolumbus&#8216; 
                        Vater Domenico und dessen Bruder Antonio &#8211; für Ren&eacute; von 
                        Anjou eingenommen. Dieser Kaufmannssohn aus Bourges war Hoflieferant des 
                        franz&ouml;sischen K&ouml;nigs und deshalb an Luxusgütern wie Edelmetallen, 
                        Seide, Gewürzen und Heilmitteln aus dem Orient besonders stark interessiert. 
                        Er brachte die franz&ouml;sisch-genuesische Handelsflotte wieder auf Vordermann 
                        und es folgte eine atemberaubende Karriere. Sein Erfolg brach abrupt ab, 
                        als ihn der franz&ouml;sische K&ouml;nig &#8211; der unterdessen zu seinem 
                        Hauptschuldner geworden war &#8211; in Haft nahm. Zwar gelang es Coeur, 
                        dem Gefängnis zu entfliehen und auf einer abenteuerlichen Flucht mehreren 
                        Mordanschlägen zu entgehen, doch er hatte seinen Zenit bereits überschritten. 
                        Immer noch auf der Flucht, begab er sich nach dem Fall Konstantinopels 1453 
                        mit dem Segen des Papstes Calixtus III. mit 16 Galeeren auf einen Kreuzzug 
                        in die Levante. Im Jahre 1456 starb Jacques Coeur auf der genuesischen Kolonie 
                        Chios. Je nach Geburtsdatum ist eine Teilnahme Kolumbus&#8217; an diesem 
                        Kreuzzug nicht ganz ausgeschlossen. Sehr wahrscheinlich reiste Kolumbus 
                        aber erst in den frühen siebziger Jahren nach Chios, als sich der franz&ouml;sische 
                        K&ouml;nig Louis XI. vornahm, den Gewürzhandel in der Levante mit Hilfe 
                        einer grossen franco-genuesischen &#8222;Compagnie&#8220; unter seine Kontrolle 
                        zu bringen.
                         
