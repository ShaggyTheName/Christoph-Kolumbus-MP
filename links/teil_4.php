                    <h4>Freundschaft im franz&ouml;sischen &#8222;Exil&#8220;</h4>
                    </div>
                    <div class="col-md-6">
                    
                            <p>Der Pal&auml;ologe Columbus der Jüngere 
      vollendete nach der Seeschlacht vom Kap von San Vicente den ursprünglichen 
      Auftrag und eskortierte die Flotte des besiegten portugiesischen K&ouml;nigs 
      <a href="http://de.wikipedia.org/wiki/Alfons_V._(Portugal)" target="_blank">Alfons 
      V.</a> nach Frankreich, in die südfranz&ouml;sische Hafenstadt Collioure 
      im Golf von Narbonne (Harrisse 1874, S. 18, Anm. 2). Die Festung von Collioure, 
      einst Tempelritterhafen und Sommerresidenz der K&ouml;nige von Mallorca, 
      geh&ouml;rte erst seit wenigen Monaten zu Frankreich und befand sich nahe 
      der Front zu Aragon. Zwischen dem portugiesischen K&ouml;nig Alfons V. mit 
      dem Beinamen der Afrikaner und seinem Begleiter Columbus dem Jüngeren 
      entstand eine tiefe Freundschaft. Alfons V. forderte den franz&ouml;sischen 
      K&ouml;nig auf, dem Pal&auml;ologen besondere Privilegien und Titel zukommen 
      zu lassen. Doch Louis XI. befahl den beiden, sich unverzüglich in den 
      &Auml;rmelkanal zu begeben: Am 14. Juli 1477 erliess er in der Hafenstadt 
      Honfleur den Befehl, auf Kosten der St&auml;dte und Kl&ouml;ster der Normandie 
      eine neue Flotte auszurüsten (Ronci&egrave;re 1914, S. 375).<br>
      Die fortlaufenden Fronteins&auml;tze widerstrebten dem portugiesischen 
      K&ouml;nig: Als die Schiffe am 23. September zum Auslaufen bereit waren, 
      war er pl&ouml;tzlich nicht mehr auffindbar. Nach intensiver Suche griffen 
      ihn seine rund 300 portugiesischen Gefolgsleute, die beinahe zu Untertanen 
      des franz&ouml;sischen K&ouml;nigs geworden waren, in einer Herberge in 
      der N&auml;he von Honfleur auf (Ronci&egrave;re 1914, S. 375). Alfons hatte 
      mit der Begründung demissioniert, eine Pilgerreise zum Franziskanerkloster 
      des Berges Sion in Pal&auml;stina unternehmen zu müssen (Milhou 1983, 
      S. 343). <br> Auch Colombo der Jüngere fiel Ende der siebziger 
      Jahre durch keine spektakul&auml;ren Eins&auml;tze mehr auf. Er beschr&auml;nkte 
      sich seinerseits darauf, von Honfleur aus die Transportwege für den 
      Nachschub der franz&ouml;sischen Truppen freizuhalten.
                          </p>
                         
