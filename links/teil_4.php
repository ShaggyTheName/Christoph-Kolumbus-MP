                    <h4>&#8222;Columbo capitano&#8220; vor Genua</h4>
                    </div>
                    <div class="col-md-6">
                    
                        <p>1478 taucht in den secreti ein &#8222;Columbo 
                        capitano et corsaro de gallee&#8220; im Mittelmeer auf, und zwar im Golf 
                        von Ligurien. Seine Mission war es, Bona von Savoyen, die Witwe des ermordeten 
                        Herzogs von Mailand, nach Frankreich in Sicherheit zu bringen. Sie war die 
                        Schwester von Louis XI.&#8217; Ehefrau Charlotte von Savoyen und hatte im 
                        Frühling 1477 vergeblich versucht, die Hafenstadt Genua mit 12&#8217; 
                        000 Mann unter franz&ouml;sische Kontrolle zu bringen &#8211; die Stadt 
                        befand sich bereits unter aragonesischem Einfluss. Als in Genua im August 
                        1478 der Bürgerkrieg ausbrach, gelang es dem Columbo capitano gerade 
                        noch rechtzeitig, die Witwe Galeazzo Maria Sforzas in die Provence zu evakuieren 
                        (Harrisse 1874, S. 22 und S. 98). 
                        </p>
                         
                    </div>

                    <div class="col-md-6">
                        <p>Drei Monate sp&auml;ter kreuzte er noch 
                        ein weiteres Mal in den Küstengew&auml;ssern Genuas auf, diesmal mit 
                        dem Auftrag, die neuen genuesischen Machthaber einzuschüchtern; diese 
                        hatten mit Hilfe Aragons, Neapels und des Papstes die Oberhand in Genua 
                        gewonnen (Salvagnini 1894, S. 167). Die Frage, ob es sich bei diesem Columbo 
                        capitano et corsaro um Christoph Kolumbus (oder seinen Bruder) gehandelt 
                        hatte, l&auml;sst sich heute nicht mehr beantworten. Denn gem&auml;ss der 
                        Überlieferung weilten die Gebrüder Colombo seit 1476 in Portugal. 
                        Es ist allerdings nicht ausgeschlossen, dass sie w&auml;hrend des neunj&auml;hrigen 
                        Friedens in den Rosenkriegen (Aufenthaltszeit des Kolumbus in Portugal) 
                        immer noch in franz&ouml;sischen Diensten standen und für den einen 
                        oder anderen Einsatz beigezogen wurden.</p>
