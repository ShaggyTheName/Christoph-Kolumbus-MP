                    </div>
                    <div class="col-md-6">
                        <p>Als der franz&ouml;sische K&ouml;nig Louis XI. 
      von der Zerst&ouml;rung seiner Flotte am Kap von San Vicente bei Sagres 
      h&ouml;rte, soll er gem&auml;ss Augenzeugen wutentbrannt gepoltert haben, 
      dass dies alles gegen seinen Willen geschehen sei (Salvagnini 1894, S. 208).<br>
      Im Sommer 1476 h&auml;tte er eine intakte Flotte 
      im Mittelmeer sehr wohl gebrauchen k&ouml;nnen &#8211; nicht zuletzt für 
      die Verteidigung der Insel Zypern, der wichtigsten christlichen Bastion 
      im &ouml;stlichen Mittelmeer. Fast 300 Jahre lang hatte das franz&ouml;sische 
      Adelsgeschlecht Lusignan mit Hilfe der Genuesen auf Zypern regiert. 1475 
      starb der uneheliche Jacques II. Lusignan eines pl&ouml;tzlichen Todes, 
      nachdem er sich gewaltsam gegen seine erbberechtigte Halbschwester Charlotta 
      durchgesetzt und die Venezianerin Caterina Cornaro geheiratet hatte. Als 
      letztere sich nun mit Hilfe der Venezianer auf der Insel durchsetzen wollte, 
      kam es zu mehreren Aufst&auml;nden. <br>
      Noch im Sommer 1476 war es der Venezianerin <a href="http://de.wikipedia.org/wiki/Caterina_Cornaro" target="_blank">Catarina 
      Cornaro</a>, der Witwe Lusignans, nicht gelungen, die Nachfolge zu regeln. 
      So wollte ein unehelicher Sohn des K&ouml;nigs Ferrante von Neapel eine 
      Tochter des verstorbenen Regenten heiraten und Zypern mit Hilfe der Genuesen 
      und des Sultans (Soldano) einnehmen. Christoph Kolumbus&#8217; Heimatstadt 
      Genua war eine zu tiefst gespaltene Stadt: Die Bewohner k&auml;mpften ihrer 
      politischen Ausrichtung zufolge entweder für die Sache Frankreichs 
      oder Aragons.<br>Wie einem Geheimbericht zweier mail&auml;ndischer Agenten 
      namens Arcimboldus und Trivultius zu entnehmen ist, kreuzte im Sommer 1476 
      ein genuesisches Schiff mit dem Namen Palavissima vor Zypern auf. Dieses 
      Schiff hatte zuvor vom Festland 500 türkische Soldaten und Kaufleute 
      an Bord geladen. Wie der Name besagt, geh&ouml;rte die Nave Pallavissima 
      der Genueser Familie Pallavicini bzw. Paravicini und diese stammten wie 
      die Colombos aus dem Lehensbezirk der Fieschis. Im September 1476 &#8211; 
      drei Monate vor der Ermordung des Herzogs von Mailand an Weihnachten &#8211; 
      gelang es den Paravicinis, in Genua die Macht zu ergreifen. Antonio Gallo 
      schweigt sich über dieses Ereignis in seiner Chronik seltsamerweise 
      aus (Gallo 1911, S. 34).</p>
                        
