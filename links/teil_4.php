                        <h4>&#8222;Colombo&#8220; im &ouml;stlichen Mittelmeer</h4>
                    </div>
                    <div class="col-md-6">
                        <p>
                          
                        Das Auftauchen der Nave Palavissima vor Zypern 
      provozierte im August 1476 zwei lange Seeschlachten mit der venezianischen 
      Flotte. Die Genuesen unterlagen mit 120 Opfern. Der venezianische Flottenführer 
      sagte gegenüber den mail&auml;ndischer Agenten aus, die Genuesen h&auml;tten 
      ihm den Warenzoll verweigert und dies mit der Begründung, dass der 
      Flottenkommandant Colombo, dem sie zuvor begegnet seien, von ihnen keinen 
      solchen Tribut abverlangt habe. Welcher Colombo &#8211; er sei mit Schiffen 
      und Galeeren (con nave et galee) unterwegs gewesen &#8211; war hier gemeint? 
      Der Gascogner Columbus Archipyrata und Columbus der Jüngere waren es 
      mit Sicherheit nicht, denn diese befanden sich zum damaligen Zeitpunkt nachweislich 
      im &Auml;rmelkanal. Am ehesten handelte es sich um denselben Collumbus, 
      der sich bereits 1473 bei Almeria aufhielt und dort venezianischen Schiffen 
      auflauerte (und der wegen seiner Verbindung zu Tunis wahrscheinlich mit 
      Kolumbus identisch war). <br>
      Die heterogene Zusammensetzung der Flotte l&auml;sst 
      auf einen Zusammenschluss der Karavellen Ren&eacute; von Anjous mit der 
      reduzierten franz&ouml;sischen Mittelmeerflotte des Admirals Jean de Villages 
      (eines Neffen von Jacques Coeur) schliessen. Es ist anzunehmen, dass diese 
      Schiffe sich im Sommer 1476 aufmachten, die franz&ouml;sische Atlantikflotte 
      bei ihrer Einfahrt ins Mittelmeer am Kap von San Vicente zu unterstützen. </p>
