                        <h4>Entspannung in Lissabon 1476</h4>
                    </div>
                    <div class="col-md-6">
                        <p>
                          
                        Nach der für beide Seiten verlustreichen Seeschlacht vom Kap von San Vicente im August 1476 versammelten sich die Franzosen im Hafen von Lagos an der Algarve; hier wurden die beschädigten Schiffe ausgebessert und manche Seeleute mussten ausgemustert werden. Der Vizeadmiral des Atlantiks, Columbus Archipyrata, nahm danach Kurs zurück nach Lissabon. Da Kolumbus’ Schiff abgebrannt war, reiste auch er in die portugiesische Hauptstadt. Von nun an löste sich das einstige Schreckensbild der französischen Korsaren Columbus auf. Gemäss einem Bericht befand sich Columbus mit seiner Atlantikflotte (armata biscaina) im Dezember 1476 in Lissabon, wo seine Schiffe abgetakelt in der Tajomündung ruhten (Salvagnini 1894, S. 209). Columbus verfüge nicht mehr über die Macht, die er einst besessen habe: Er habe mit neun Karavellen erneut am Kap von San Vicente gelagert, doch gegen eine 40 Schiffe starke, venezianischen Flotte nichts ausrichten können. 
An Weihnachten 1476 wurde der (abtrünnige) Herzog von Mailand ermordet, und Genua geriet wieder unter französischen Einfluss. Ein in Lissabon stationierter Agent schrieb an den Sekretär der verwitweten Herzogin von Mailand, Bona von Savoyen, dass die genuesischen Kaufleute Spinola und Di Negro in Lissabon einem Colombo begegnet seien, von dem man sich einig sei, dass er aus Savona stamme (... e dacordò e dicto Saonese).
Dieser habe sich mit ihnen anfreunden wollen, weil sie unterdessen ja Freundesfreunde (amigo de lo amigo) geworden seien. Dass der spätere Entdecker Amerikas nach der unheilvollen Konfrontation mit seinen Genueser Landsleuten Kontakt aufnahm, ist nicht ausgeschlossen. Kolumbus gedachte den Nachkommen der am Kap von San Vicente geschädigten Kaufleute Spinola und Di Negro in seinem Testament des Jahres 1501. </p>
