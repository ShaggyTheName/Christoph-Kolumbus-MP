                        <h4>Expedition nach &quot;oltra Tile&quot;</h4>
                    </div>
                    <div class="col-md-6">
                    <p>Kolumbus nahm im Februar 1477 an einer Fahrt in den n&ouml;rdlichen 
      Westatlantik teil. Gem&auml;ss eigenen Angaben segelte er über Island 
      hinaus, bis zu einer Insel von der Gr&ouml;sse Englands. Für die siebziger 
      Jahre ist eine d&auml;nisch-portugiesische Expedition dokumentiert, die 
      bis nach Gr&ouml;nland gelangt war. Die Armada wurde ausgerüstet, um 
      den Engl&auml;ndern bzw. den Seeleuten aus Bristol Einhalt zu gebieten; 
      diese segelten in den Westatlantik und stiessen bis weit in d&auml;nische 
      Fischgründe vor. Die Aufgabe der Ko-Expedition war es, das Hoheitsgebiet 
      der D&auml;nen neu zu markieren und an der Ostküste Gr&ouml;nlands 
      Grenzsteine zu setzen. Siehe hierzu den Artikel in englischer Sprache:<a href="essay.html" target="mainFrame"> 
      &#8222;Was Christopher Columbus in Greenland before he discovered America?&#8220; 
      (siehe: Essay) </a></p>

