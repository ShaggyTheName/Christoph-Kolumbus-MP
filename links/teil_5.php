                        <h4>Sagen und Legenden des Atlantiks</h4>
                    </div>
                    <div class="col-md-6">
                        <p>
                          
                        Gem&auml;ss Hernando Col&oacute;n liess sich Christoph Kolumbus 
      von der Sankt Brendans Saga der irischen M&ouml;nche stark beeindrucken. <br>
      Die Sage knüpft an die Endzeitmystik der sp&auml;tantiken 
      Christenverfolgungen an und besagt, dass der heilige Brendan (ca. 486&#8212;580 
      n. Chr.) aus der westirischen Grafschaft Galway mit seinen M&auml;nnern 
      auf das Meer hinaus segelte, um im Westen das verheissene Land der Heiligen 
      (terra repromissionis sanctorum) zu entdecken. Die irischen Seefahrerpilger 
      kannten damals noch die ptolem&auml;ische Weltauffassung und wussten um 
      die Kugelform der Erde; eine Atlantiküberquerung ist deshalb nicht 
      ausgeschlossen.<br>
      Kolumbus kannte auch die Legende der sieben christlichen 
      Bisch&ouml;fe: Diese waren angeblich in der ersten H&auml;lfte des 8. Jahrhunderts, 
      zur Zeit der maurischen Eroberung Portugals, auf Schiffen in den Atlantik 
      hinaus geflohen. Nach einer abenteuerlichen Fahrt ins offene Meer hinaus 
      sollen sie auf der Insel Antilia (anta-ilha = Gegeninsel) gelandet sein, 
      wo sie sieben christliche St&auml;dte gründeten.</p>
