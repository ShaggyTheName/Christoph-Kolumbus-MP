                        <h4>Die Jahre in Portugal (1476&#8212;1485)</h4>
                    </div>
                    <div class="col-md-6">
                        <p>Christoph Kolumbus heiratete in Portugal Felipa Mo&ntilde;iz 
      Perestrello, eine Frau aus altem portugiesisch-franz&ouml;sischen Kreuzrittergeschlecht. 
      Sie wurde die Mutter seines &auml;ltesten Sohnes und sp&auml;teren Nachfolgers 
      Diego Col&oacute;n. <br>
      <p>
                          
                          Das Auftauchen der Nave Palavissima vor Zypern 
        provozierte im August 1476 zwei lange Seeschlachten mit der venezianischen 
        Flotte. Die Genuesen unterlagen mit 120 Opfern. Der venezianische Flottenführer 
        sagte gegenüber den mail&auml;ndischer Agenten aus, die Genuesen h&auml;tten 
        ihm den Warenzoll verweigert und dies mit der Begründung, dass der 
        Flottenkommandant Colombo, dem sie zuvor begegnet seien, von ihnen keinen 
        solchen Tribut abverlangt habe. Welcher Colombo &#8211; er sei mit Schiffen 
        und Galeeren (con nave et galee) unterwegs gewesen &#8211; war hier gemeint? 
        Der Gascogner Columbus Archipyrata und Columbus der Jüngere waren es 
        mit Sicherheit nicht, denn diese befanden sich zum damaligen Zeitpunkt nachweislich 
        im &Auml;rmelkanal. Am ehesten handelte es sich um denselben Collumbus, 
        der sich bereits 1473 bei Almeria aufhielt und dort venezianischen Schiffen 
        auflauerte (und der wegen seiner Verbindung zu Tunis wahrscheinlich mit 
        Kolumbus identisch war). <br>
        Die heterogene Zusammensetzung der Flotte l&auml;sst 
        auf einen Zusammenschluss der Karavellen Ren&eacute; von Anjous mit der 
        reduzierten franz&ouml;sischen Mittelmeerflotte des Admirals Jean de Villages 
        (eines Neffen von Jacques Coeur) schliessen. Es ist anzunehmen, dass diese 
        Schiffe sich im Sommer 1476 aufmachten, die franz&ouml;sische Atlantikflotte 
        bei ihrer Einfahrt ins Mittelmeer am Kap von San Vicente zu unterstützen. <br>
      
        Seine Schwiegermutter hiess Isabel Mo&ntilde;iz 
        und war die Witwe Bartolomeu Perestrellos, des verstorbenen Gouverneurs 
        der Insel Porto Santo. Die Perestrellos waren ebenfalls Adlige: Sie stammten 
        ursprünglich aus Italien und geh&ouml;rten zum Umkreis Heinrichs des 
        Seefahreres (siehe:<a href="teil_2.html">Mitglied des Templerordens?</a>). 
        Bartolomeus Bruder Richarte war Prior von Santa Marinha zu Lissabon und 
        seine zwei Schwestern Isabel und Franca waren die M&auml;tressen des einflussreichen 
        Erzbischofs Pedro de Noronha.<br>
        Gem&auml;ss Hernando Col&oacute;n zeigte Isabel Mo&ntilde;iz 
        dem zukünftigen Entdecker die Seekarten ihres verstorbenen Mannes Bartolomeu 
        Perestrello.  <br>
        Christoph Kolumbus und sein Bruder Bartolomeo nahmen in den 
        folgenden Jahren an mindestens einer Expedition nach Westafrika teil und 
        wurden dabei auf ein faszinierendes Projekt aufmerksam: auf die Erschliessung 
        der lukrativen M&auml;rkte Asiens und Indiens auf dem Seewege. Kolumbus 
        nahm Kontakt mit dem Florentiner Arzt <a href="http://de.wikipedia.org/wiki/Paolo_dal_Pozzo_Toscanelli" target="_blank">Paolo 
        dal Pozzo Toscanelli</a> (1397&#8211;1487) auf, der Kolumbus postwendend 
        zu seinem &#8222;edlen und grossen Wunsch&#8220; gratulierte, den Atlantik 
        überqueren zu wollen, um den Grosskhan bzw. den &#8222;K&ouml;nig der 
        K&ouml;nige&#8220; zu sprechen. Toscanelli liess ihm von Rom aus eine (heute 
        verlorene) Seekarte nach Portugal schicken.<br>
        Im Nachhinein bezeichnete Christoph Kolumbus seine Strandung 
        am Kap von San Vicente 1476 als ein wundersames Zeichen Gottes. Auf dem 
        H&ouml;hepunkt seiner Macht zeichnete er eine Skizze für ein Gem&auml;lde, 
        das mit den Stilmitteln der Renaissance die historische Bedeutsamkeit seiner 
        Entdeckungsfahrten illustrieren sollte.<br>
        Das Gem&auml;lde wurde nie gemalt und ist dennoch aussagekr&auml;ftig: 
        Auf dem Bild steuert Colombo (Figur rechts) eine Karavelle durch die Wogen; 
        dies unter einem schwebenden Engel, auf dessen Fanfarenbanner Genova (oben) 
        geschrieben steht. Neben ihm sitzt auf gleicher H&ouml;he der Rey Cat&oacute;lico 
        Ferdinand von Aragon (Figur links). Allerdings sind es weniger die Winde, 
        die das Schiff vorantreiben, denn eine explosive Mischung aus Feuer und 
        Rauch im Schiffsrumpf.</p>
