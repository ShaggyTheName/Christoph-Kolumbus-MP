                    <h4>Das Ende der Korsaren</h4>
                    </div>
                    <div class="col-md-6">
                    
                            <p>Nach dem Tod Karls des Kühnen 1477 bei Nancy liess der 
      franz&ouml;sische K&ouml;nig Louis XI. Burgund besetzen, doch es gelang 
      ihm nicht, das reiche Flandern einzunehmen. Guillaume de Casenove alias 
      Colomb soll im Winter 1478/79 zusammen mit anderen Seer&auml;ubern (escumeurs 
      de mer) von der Normandie aus bis zu achtzig flandrische Schiffe gekapert 
      haben (Salvagnini 1894, S. 167). Danach zog er sich 1480 auf sein Landgut 
      Gaillartbois in der N&auml;he von Rouen zurück, wo er kurz darauf starb. 
      Der franz&ouml;sische K&ouml;nig hatte am 9. Januar 1478 einen Frieden mit 
      Venedig geschlossen; von nun an war der Nutzen der einst gefürchteten 
      Korsaren g&auml;nzlich in Frage gestellt. Zwar unterzeichnete Louis XI. 
      am 10. Juli 1483 eine letzte Verordnung für den Bau von neuen Schiffen 
      und Galeeren &#8211; er wollte seinen Traum einer m&auml;chtigen Levante-Flotte 
      verwirklichen (Ronci&egrave;re 1914, S. 390). Doch die Tage des Monarchen 
      waren gez&auml;hlt: Seit M&auml;rz 1481 litt er an heftigen Schüttelanf&auml;llen 
      und bereits im September 1482 hatte er in Florenz nach einer Arznei gegen 
      Hautgeschwüre (poix loupins) suchen lassen; laut der chronique scandaleuse 
      Jean de Royes schreckte <a href="http://de.wikipedia.org/wiki/Ludwig_XI._(Frankreich)" target="_blank">Louis 
      XI.</a> selbst von den fragwürdigsten Wundermitteln nicht zurück.<br>
      Schliesslich schickte er am 8. Juli 1483 Columbus den Jüngeren 
      mit drei Schiffen und 300 Mann Besatzung auf die Kapverdischen Inseln, damit 
      man ihm von dort das Fleisch von Wasserschildkr&ouml;ten besorge &#8211; 
      gem&auml;ss damaliger Auffassung vermochte dieses die Lepra zu heilen (Anghiera 
      1972, Buch VI, S. 84). Doch der franz&ouml;sische K&ouml;nig starb noch 
      im Jahr 1483 &#8211; mehrere Monate, bevor Georg Pal&auml;olog alias Columbus 
      der Jüngere von der westafrikanischen Küste zurückkehrte. 
      Dass der sp&auml;tere Entdecker ihn dorthin begleitet hatte, ist nicht ganz 
      ausgeschlossen; denn Kolumbus hatte sich zwischen 1478 und 1484 tats&auml;chlich 
      auf den Kapverdischen Inseln aufgehalten.
                          </p>
                         
