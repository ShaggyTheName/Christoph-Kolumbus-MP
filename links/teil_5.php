                    <div class="col-md-12">
                    <h4>Flucht nach Andalusien</h4>
                    </div>
                    <div class="col-md-6">
                    
                        <p>1484 lief der in den Rosenkriegen geschlossene Friede von 
      Picquigny ab und es herrschte wieder Krieg zwischen England und Frankreich. 
      So kam es im Winter 1484/85 zu einer zweiten grossen Seeschlacht in der 
      N&auml;he des Kaps von San Vicente im Südwesten Portugals. Am Vortag 
      der <a href="http://de.wikipedia.org/wiki/Schlacht_von_Bosworth_Field" target="_blank">Schlacht 
      bei Bosworth</a> im englischen Leicestershire (21. August 1485) er&ouml;ffneten 
      unter dem Oberbefehl von Columbus dem Jüngeren sieben Schiffe das Feuer 
      auf vier Handelsschiffe der Republik Venedig, die von Flandern zurückkehrten.<br>
      Wie den venezianischen Geheimdienstakten zu entnehmen ist, 
      war im August 1485 auch ein Cristoforo Colombo corsaro mit von der Partie, 
      der im Auftrag von<a href="http://de.wikipedia.org/wiki/Sixtus_IV." target="_blank"> 
      Papst Sixtus IV.</a> gehandelt habe; der Savoneser Papst hatte die Republik 
      Venedig tats&auml;chlich kurz zuvor exkommuniziert &#8211; wegen der für 
      den Kirchenstaat zu expansiven Oberitalienpolitik Venedigs (Salvagnini 1894, 
      S. 132.). Hernando Col&oacute;n, der pauschalisierend von gegnerischen Schiffen 
      Venedigs spricht, vermengte diese Seeschlacht mit der früheren von 
      1476. <br>
      Der portugiesische <a href="http://de.wikipedia.org/wiki/Johann_II._(Portugal)" target="_blank">K&ouml;nig 
      Jo&atilde;o II</a>. unterstützte die angegriffenen Venezianer mit allen 
      Mitteln, als diese in Lissabon eintrafen &#8211; davon wusste auch Hernando 
      Col&oacute;n zu berichten, der seine Informationen vom venezianischen Chronisten 
      Sabellico bezog.<br>
      Tats&auml;chlich hatte er sich im Jahre 1484 bereits mit 
      der m&auml;chtigen Seestadt Venedig verbündet. Ein Verbleib in Portugal 
      war für Christoph Kolumbus ab 1485 aus verschiedenen Gründen nicht 
      mehr denkbar: In Portugal war es zu Adelserhebungen gegen die Regierung 
      Jo&atilde;o II. gekommen, und die portugiesische Aristokratie hatte sich, 
      um der blutigen Rache des K&ouml;nigs zu entgehen, teilweise bereits ins 
      benachbarte Kastilien abgesetzt. Kolumbus floh mit seinem noch minderj&auml;hrigen 
      Sohn im Winter 1484/85 ebenfalls nach Andalusien. Die Algarve war ein altes 
      Besitztum der Familie Mo&ntilde;iz und im Grenzgebiet zwischen Portugal 
      und Spanien hatten immer noch die Herz&ouml;ge und Adligen das Sagen. <br>
      Violante Mo&ntilde;iz, die Schwester von Kolumbus&#8217; 
      unterdessen verstorbener Ehefrau Felipa Mo&ntilde;iz Perestrello, hielt 
      sich bereits in Palos de la Frontera am Ufer des Rio Tinto auf. In der N&auml;he 
      befand sich das Kloster La Rabida, wo Kolumbus&#8217; Projekt einer Atlantiküberquerung 
      mit grossem Interesse aufgenommen wurde. <br>
      Der Genueser Seefahrer suchte den Kontakt mit dem kastilischen Hof. Doch 
      als er sein Pl&auml;ne vorstellte, verh&ouml;hnten ihn angeblich alle Anwesenden 
      &#8211; mit Ausnahme der <a href="http://de.wikipedia.org/wiki/Isabella_I._(Kastilien)" target="_blank">K&ouml;nigin 
      Isabella von Kastilien</a>, der Gott &#8222;espirit&oacute; de inteligen&ccedil;ia&#8220; 
      verliehen habe. Dieser Begriff bezieht sich auf das Verst&auml;ndnis tiefer 
      liegender, religi&ouml;ser Zusammenh&auml;nge und steht in Zusammenhang 
      mit der endzeitlichen Prophetie des süditalienischen Zisterzienser-Abt 
      <a href="http://de.wikipedia.org/wiki/Joachim_von_Fiore" target="_blank">Joachim 
      di Fiore</a> (gest. 1202). Laut Kolumbus soll di Fiore gesagt haben, dass 
      derjenige, der auserw&auml;hlt sei, das Haus des Berges Zion wiederaufzubauen, 
      von Spanien aus aufbrechen müsse.<br>
      Kolumbus&#8217; Projekt wurde in der Folge kontrovers diskutiert: 
      vom Befürworter Fray Diego de Deza und von Hernando de Talavera, dem 
      sp&auml;teren Erzbischof von Granada (letzterer war ein entschiedener Gegner 
      des Projekts). Kolumbus trat in den Sold der spanischen Krone und beteiligte 
      sich an der Reconquista, der christlichen Wiedereroberung Spaniens: Er nahm 
      an den Belagerungen Murcias, Malagas und Bazas teil. Sein Bruder Bartolomeo 
      Colombo befand sich immer noch in Portugal und nahm an der Erstumsegelung 
      des Kaps der Guten Hoffnung unter dem Kommando des portugiesischen Kapit&auml;ns 
      <a href="http://de.wikipedia.org/wiki/Bartolomeu_Diaz" target="_blank">Bartolomeu 
      Dias</a> teil. <br>
      Als die Seefahrer zurückerwartet wurden, liess <a href="http://de.wikipedia.org/wiki/Johann_II._(Portugal)" target="_blank">K&ouml;nig 
      Jo&atilde;o II.</a> am 20. M&auml;rz 1488 einen Freibrief an Christoph Kolumbus 
      überbringen. In diesem bezeichnet er ihn als unseren besonderen Freund 
      (noso especial amigo), dessen Tüchtigkeit und Talent man in Portugal 
      immer noch gebrauchen k&ouml;nnte; trotz einiger Angelegenheiten, in die 
      er, Kolumbus, verwickelt gewesen sei, habe er seitens der portugiesischen 
      Justiz nichts mehr zu befürchten: Dieses Schreiben sei Befehl an alle 
      Gerichtspersonen, ihn nicht zu behelligen. Es ist ungewiss, ob Kolumbus 
      in der Folge nach Lissabon reiste, um seinen zurückkehrenden Bruder 
      zu treffen. Denn im selben Jahr gebar ihm die Cordobesin Beatrice Henr&iacute;quez 
      de Harana seinen zweiten Sohn Hernando Col&oacute;n. <br>
      1491 wurde Christoph Kolumbus in Kastilien ein weiteres Mal 
      zurückgewiesen. Der p&auml;pstliche Legat Alessando Geraldini da Amelia 
      erinnert sich sp&auml;ter, Kolumbus habe sich in einen theologischen Disput 
      verstricken lassen und sei in der Folge sogar der Ketzerei bezichtigt worden. 
      Nach der Einnahme der Stadt Granada im Frühjahr 1492 wollte sich Kolumbus 
      nach Frankreich einschiffen lassen, denn dort hatte sein Bruder angeblich 
      erfolgreich mit Louis XI.&#8217; Tochter Anne de Beaujeu verhandelt. Spanische 
      Herolde griffen ihn jedoch kurz vor seiner Abreise auf und brachten ihn 
      ins Heerlager von Santa F&eacute; bei Granada; dort handelte er mit den 
      Reyes Cat&oacute;licos <a href="http://de.wikipedia.org/wiki/Isabella_I._(Kastilien)" target="_blank">Isabella 
      von Kastilien </a>und Ferdinand von Aragon den berühmten Vertrag von 
      Santa F&eacute; aus. <br>
      Dieser Vertrag von Santa F&eacute; wurde im April 1492 unterzeichnet, 
      im selben Monat als auch die Ausweisung von Juden und Moslems aus Spanien 
      angeordnet wurde. Die im Vertrag von Santa F&eacute; enthaltenen, weitreichenden 
      Privilegien für Kolumbus (Admirals- und Vizek&ouml;nigstitel, finanzielle 
      Beteiligung etc.) waren erblich und hatten auch für seine Nachkommen 
      Gültigkeit.</p>
