                    <div class="col-md-12">
                    <h4>Der religi&ouml;se Hintergrund der Epoche</h4>
                    </div>
                    <div class="col-md-6">
                    
                        <p>Auf der letzten Reise gab Kolumbus an, die Worte Gottes vernommen 
      zu haben. Der Glaube, in direktem Kontakt zu Gott zu stehen und gewisse 
      Taten vollbringen zu müssen, war nichts Ungew&ouml;hnliches in der 
      damaligen Epoche. An verschiedenen Orten Europas kam es in jener Zeit zu 
      messianischen Bewegungen mit charismatischen Anführern. Ein Beispiel 
      ist der Dominikaner <a href="http://de.wikipedia.org/wiki/Girolamo_Savonarola" target="_blank">Girolamo 
      Savonarola</a> (geb. 1452, hingerichtet in Florenz 1498) . Er wandte sich 
      vehement gegen die Verweltlichung der Kirche und sorgte mit seinen Predigten 
      in ganz Norditalien für Aufruhr. <br>
      Die P&auml;pste unterschieden sich damals tats&auml;chlich nur wenig von 
      den weltlichen Potentaten: Sie boten wegen ihrer Zinspolitik und Vetternwirtschaft 
      ein Bild, welches nur allzu gut zur Offenbarung des Johannes passte, wo 
      Babylon mit Rom identifizierbar ist (vgl. Off. 17, 18.9: die Herrschaft 
      der Stadt über die Welt und ihre Lage auf sieben Bergen). Savonarola 
      fühlte sich von Gott dazu berufen, nach dem Sturz der Medici im Jahre 
      1494 in Florenz eine Art theokratische &quot;Demokratie&quot; zu errichten. 
      Als der franz&ouml;sische K&ouml;nig Karl VIII. mit seinen Truppen einmarschierte, 
      um das ihm zugefallene Erbe Neapel-Sizilien zu erobern (siehe: Im Dienste 
      Ren&eacute;s von Anjou), feierte Savonarola ihn als Erl&ouml;ser und Befreier 
      des guelfischen Italiens. Doch 1498 wurde Savonarola exkommuniziert, gefangen 
      genommen und als H&auml;retiker und Schismatiker in Florenz verbrannt.<br>
      Kolumbus untersch&auml;tzte die Gr&ouml;sse des Atlantiks trotz eigener, 
      einschl&auml;giger Erfahrung um ein Vielfaches. Er berief sich bei seinen 
      realit&auml;tsfremden Berechnungen auf das apokryphe vierte Buch Esdras&#8217;. 
      Dieses war wegen seiner mystischen Symbolik im ausgehenden Mittelalter sehr 
      beliebt. Der Florentiner Gelehrte Giovanni Pico della Mirandola (1463 &#8211;1494) 
      hatte sich mit dessen kabbalistischem Inhalt befasst und suchte in der Kabbala 
      nach der Urform menschlichen Denkens. Er schrieb den Menschen wegen seinen 
      sch&ouml;pferischen F&auml;higkeiten als gott&auml;hnliches Wesen und verknüpfte 
      jüdische, islamische und christliche Elemente des philosophischen Denkens 
      miteinander.</p>
