                    <h4>Das Buch der Prophezeiungen</h4>
                    </div>
                    <div class="col-md-6">
                    
                        <p>Kolumbus verfasste mit Hilfe seines Sohnes Hernando und seines 
      Bruders Bartolomeo auf seiner letzten Reise das Buch der Prophezeiungen 
      (libro de las Profec&iacute;as). Darin enthalten sind Psalme, apokryphe 
      Bibelstellen und Textfragmente, die seine Entdeckung in einen prophetischen 
      Horizont stellen. Von den 84 Bl&auml;ttern (folios) sind allerdings 14 herausgetrennt 
      worden. Der spanische Historiker Ambrosio de Morales, der in der zweiten 
      H&auml;lfte des 16. Jahrhunderts Kolumbus&#8217; libro sichtete, klagte, 
      dass diese Seiten das Beste der Prophezeiungen beinhaltet h&auml;tten. Kolumbus 
      machte seine Entdeckungsfahrt zur Offenbarung: Er stellte sie in den Kontext 
      der Jesaja-Prophetie und der Johannes-Apokalypse, wo von einem &#8222;neuen 
      Himmel&#8220; und einer &#8222;neuen Erde&#8220; (21) die Rede ist, vom 
      Untergang der &#8222;alten Welt&#8220; und Aufstieg des neuen Jerusalem. 
      Zu einem Zeitpunkt, als Kolumbus noch reelle Chancen auf ein prosperierendes 
      Vizek&ouml;nigtum gehabt hatte, schrieb er, dass er die Mühen und Strapazen 
      nicht auf sich nehme, um Ehre, Reichtum und andere irdische Güter anzuh&auml;ufen; 
      er wisse, wie eitel alles sei, was man in der Welt tue, mit Ausnahme dessen, 
      was man zu Ehren und im Dienste Gottes vollbringe.
    </p>
