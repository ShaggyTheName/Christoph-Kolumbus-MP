                        <h4>Die erste Fahrt nach &#8222;las Indias&#8220; 1492</h4>
                    </div>
                    <div class="col-md-6">
                        <p>Die spanischen K&ouml;nige bewilligten nach der Unterzeichnung 
      des Vertrags von Santa F&eacute; im April 1492 die Ausrüstung von drei 
      Schiffen: der Santa Maria (Flaggschiff) der Ni&ntilde;a und der Pinta. Auch 
      liessen sie ein Empfehlungsschreiben an den Grossen Khan von China verfassen. <br>
      <p>
                          
      Auf dem Weg zu den Kanarischen Inseln wurden zwei Schiffe 
      leck und mussten neu kalfatert werden. Nach einer langen, von Windstillen 
      gepr&auml;gten Fahrt ins Ungewisse, welche die Seeleute bis an den Rand 
      der Meuterei führte, wurde am 12. Oktober 1492 eine Insel im Gebiet 
      der heutigen Bahamas gesichtet. W&auml;hrend der anschliessenden Entdeckungsreise 
      musste Kolumbus seine hohen Erwartungen mehrmals revidieren: Weder wurde 
      er von Gesandten des chinesischen Fürsten empfangen noch entdeckte 
      er die von Marco Polo einst beschriebenen Reichtümer.<br>
      Dafür kam es zu Begegnungen mit teils neugierig-zudringlichen, 
      teils furchtsam-misstrauischen Eingeborenen. Kolumbus stellte ethnische 
      Unterschiede fest und begann, die Indios in gute Tainos und b&ouml;se Kariben, 
      bzw. Kannibalen einzuteilen. Nachdem Kolumbus&#8217; Flaggschiff Santa Maria 
      an Weihnachten auf eine Sandbank aufgelaufen war, musste er 39 seiner Seeleute 
      auf der Insel Espa&ntilde;ola (Santo Domingo/Haiti) zurücklassen.<br>
      Nach mehreren heftigen Stürmen auf der Rückreise, die über 
        die Azoren führte, erreichte er auf der l&auml;dierten Karavelle 
        Ni&ntilde;a schliesslich den Hafen von Lissabon, wo er vom Seefahrer Bartolomeu 
        Dias empfangen wurde. Er nahm eine Einladung von K&ouml;nig Jo&atilde;o 
        II. an und bot sich diesem als Vermittler zwischen Spanien und Portugal 
        an. Der K&ouml;nig lehnte ab und Kolumbus brach unverzüglich nach 
        Kastilien auf.
    </p>
