                    <h4>Die letzte Fahrt (1502&#8212;1504)</h4>
                    </div>
                    <div class="col-md-6">
                    
                            <p>Auf seiner letzten, zweieinhalb Jahre dauernden Reise legte 
      Kolumbus trotz Verbot im Hafen von Santo Domingo an; dort bereiteten sich 
      seine &auml;rgsten Feinde, der Meuterer Rold&aacute;n und der Regierungsbeamte 
      Bobadilla, gerade auf ihre Rückreise nach Kastilien vor. Sie liessen 
      sich auf der Karavelle von Kolumbus&#8217; Vertrauensmann Antonio de Torros&#8217; 
      einschiffen. Als die Flotte aus dem Hafen auslief, sank gerade dieses Schiff. 
      Es ist nicht auszuschliessen, dass sich darauf auch alle Dokumente Kolumbus&#8217; 
      befanden, die Bobadilla 1500 konfisziert hatte (es ist kein einziges Schriftstück 
      vor 1492 überliefert). Christoph Kolumbus nahm in der Folge Kurs auf 
      Mittelamerika, wo er vergeblich nach einer Durchfahrt in den Indischen Ozean 
      suchte &#8211; sein Ziel, die Welt zu umsegeln, war damit gescheitert. Er 
      und seine Mannschaft wurden von Hurrikans heimgesucht. Bei Panama segelte 
      die Flotte in einen Flusslauf hinein &#8211; in der irrigen Vorstellung, 
      es k&ouml;nnte sich um die im Laufe der Zeit versandete Strasse von Malakka 
      handeln.<br>
      Zurück auf offener See, begann Kolumbus mit Gott zu hadern. Er schildert 
      ein apokalyptisches Szenario:<em> &#8222;So fuhr ich in diesem Meer umher, 
      das flüssiges Blut schien und das kochte wie ein Kessel, unter dem 
      grosses Feuer brennt. Niemals ward der Himmel so schauerlich gesehen: Einen 
      Tag und eine Nacht brannte alles, als sei es ein glühender Ofen. Feuer 
      und Blitz fielen allenthalben, und jedes Mal hielt ich Ausschau, ob es mir 
      Mast und Segel geraubt h&auml;tte &#8230; In all jener Zeit h&ouml;rte es 
      niemals auf, vom Himmel zu giessen, denn man kann das nicht mehr regnen 
      nennen; eine zweite Sintflut schien begonnen zu haben.&#8220; </em>(Columbus 
      1991, Bd. 2, S. 211). <br>
      Kolumbus und seine Mannschaft, darunter sein minderj&auml;hriger 
      Sohn Hernando und sein Bruder Bartolomeo, retteten sich auf ihren von Bohrwürmern 
      durchl&ouml;cherten Schiffen bis nach Jamaika. Dort begann eine entbehrungsvolle 
      Wartezeit auf Rettung (Juni 1503&#8211;August 1504). Bald meuterte ein Teil 
      der Spanier und die Indios verloren ihren Respekt vor dem alten Admiral, 
      der an einer starken Gicht litt und ans Bett gebunden war. Dank einer astronomischen 
      Berechnung sah Kolumbus eine Mondfinsternis voraus; er deutete diese vor 
      den versammelten Indios als Zorn Gottes und sicherte sich dadurch erneut 
      ihres Respektes und ihrer Unterstützung.
                          </p>
                         
