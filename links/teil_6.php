                    <h4>Kreuzzugsmentalit&auml;t</h4>
                    </div>
                    <div class="col-md-6">
                    
                        <p>Der Genueser Seefahrer sprach mehrfach vom Unternehmen (negocio) des Wiederaufbaus 
      des heiligen Grabes. Er auferlegte seinen Nachkommen die Pflicht, zum Zwecke 
      der Befreiung Jerusalems Geld bei dem Genueser Banco di San Giorgio anzulegen. 
      In einem Brief an <a href="http://de.wikipedia.org/wiki/Alexander_VI." target="_blank">Papst 
      Alexander VI.</a> schrieb Kolumbus 1502, dass die Entdeckungsfahrten allein 
      deshalb unternommen worden seien, um &#8222;der heiligen Kirche das heilige 
      Grab zurückzugeben.&#8220; Er habe dem K&ouml;nig und der K&ouml;nigin 
      versprochen, insgesamt 100&#8217;000 Mann Fussvolk und 10&#8217;000 Reiter 
      zu diesem Zwecke auszurüsten. Vieles in Kolumbus&#8217; religi&ouml;sem 
      Weltbild erinnert an die Haltung der Kreuzritter.<br>
      Doch es gibt auch Parallelen zu den spanischen Franziskanern, 
        die unter der Führung des radikalen Reformkardinals Jim&eacute;nez 
        de Cisneros standen. <a href="http://bautz.de/bbkl/x/ximenes_f.shtml" target="_blank">Jim&eacute;nez 
        de Cisneros </a> war in Kastilien als Nachfolger Pedro Gonz&aacute;lez&#8217; 
        de Mendoza der m&auml;chtigste Mann neben K&ouml;nigin Isabella. Er gründete 
        1498 die Universit&auml;t Alcal&aacute; de Hernares und gab eine mehrsprachige 
        Bibel in Auftrag. Das ehrgeizige Übersetzungswerk, zu dem auch jüdische 
        Talmudkenner (trotz Verfolgung) beigezogen wurden, wurde vom Papst erst 
        nach dem Tod des Kardinals zur Ver&ouml;ffentlichung freigegeben; danach 
        geriet die Bibel in Vergessenheit.<br>
        <a href="http://bautz.de/bbkl/x/ximenes_f.shtml)" target="_blank">Jim&eacute;nez 
      de Cisneros</a> finanzierte 1509, drei Jahre nach Kolumbus&#8217; Tod, einen 
      Kreuzzug entlang der Küste Nordafrikas. Dieser führte zur Einnahme 
      der nordafrikanischen Stadt Oran, was euphorisch als Vorstufe zur Einnahme 
      Jerusalems gefeiert wurde. Noch auf seinem Sterbebett ernannte Cisneros 
      <a href="http://de.wikipedia.org/wiki/Bartolom%C3%A9_de_Las_Casas" target="_blank">Bartolom&eacute; 
      de las Casas</a> zum Anwalt (procurador) bzw. Schutzherrn (protector universal) 
      für Westindien &#8211; er setzte damit den Grundstein für eine 
      der gr&ouml;ssten Kontroversen der Geschichte <a href="teil_7.html" target="mainFrame">(siehe: 
      Negativbilder heute und früher/Schlussbemerkungen)</a>
    </p>
