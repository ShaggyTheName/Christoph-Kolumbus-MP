                    <div class="row">
                    <div class="col-md-12">
                    <h4>SCHLUSSWORT</h4>
                    </div>
                    <div class="col-md-6">
                    
                        <p>Christoph Kolumbus&#8217; Werdgang ist r&auml;tselhaft: Wie 
      war es m&ouml;glich, dass ein unbekannter Seefahrer ausl&auml;ndischer Herkunft 
      sich von den spanischen K&ouml;nigen den Vizek&ouml;nigs-, den Admirals- 
      und Gouverneurstitel ausbedingen konnte? Kolumbus schrieb sich prophetische 
      F&auml;higkeiten zu und beliebte, seine Entdeckung als Werk der g&ouml;ttlicher 
      Vorhersehung zu pr&auml;sentieren. Die rasche Übersetzung seines euphorisch-propagandistischen 
      Berichts der ersten Entdeckungsreise ins Lateinische (Rom 1493), Italienische 
      und Deutsche (Basel 1494) erweckte internationale Aufmerksamkeit. <br>
      Kolumbus musste, wollte er das Überleben seiner Familie in Spanien 
      sichern, auch vieles verschweigen. Er hatte gute Gründe, sich zurückzuhalten, 
      wenn es darum ging, seine Vergangenheit zu erhellen. Seit dem Vorwurf des 
      Hochverrats und seiner Entmachtung (1500) schwieg er sich über sein 
      früheres Engagement gegen Aragon aus. Nach seinem Tod wurde es noch 
      schwieriger, seinen ursprünglichen Werdegang zu erhellen. Zwar konnte 
      Hernandos Chronik als kleinformatige Übersetzung gedruckt werden (1571) 
      und darin sind durchaus Aussagen enthalten, die auf Kolumbus direkt zurückgehen. 
      Doch der Kolumbussohn hatte sich mit der spanischen und italienischen Geschichtsschreibung 
      angelegt; und zudem erhielt er Unterstützung von der &#8222;falschen&#8220; 
      Seite: vom &#8222;Nestbeschmutzer&#8220; Bartolom&eacute; de Las Casas.<br>
      Aus der Chronik Hernando Col&oacute;ns lassen sich einige interessante Details 
      herauslesen: Christoph Kolumbus, seine Brüder und seine Vorfahren geh&ouml;rten 
      einst zu den Gefolgsleuten der mittelalterlichen Dynastie der Anjous; sie 
      dürften deshalb in der Wissenstradition dieses Hauses gestanden haben. 
      Dieses Fürstengeschlecht hatte Europa in kultureller und politischer 
      Hinsicht Jahrhunderte lang gepr&auml;gt. <br>
      Die Anjous starben beim Versuch, Aragon milit&auml;risch die Stirn zu bieten, 
      aus und die Provence und Süditalien fielen an Frankreich. Kolumbus 
      wechselte in franz&ouml;sische Dienste und gelangte dadurch nach Portugal, 
      wo er seiner zukünftigen Frau Felipa Perstrello Moniza begegnete. Diese 
      Adlige altem franz&ouml;sischem und italienischem Kreuzrittergeschlecht 
      stammte aus dem Umfeld Heinrichs des Seefahrers und verhalf ihm zu dem damaligen 
      (Geheim-)Wissen bezüglich des Atlantiks. Nach einem Regierungswechsel 
      musste Kolumbus Portugal schnell verlassen; er floh mit seinem Sohn Diego 
      nach Andalusien, wo er sich den katholischen K&ouml;nigen in der Reconquista 
      andiente. Nach der Einnahme Granadas 1492 erhielt er den Zuschlag für 
      seine legend&auml;re Atlantiküberqerung.<br>
      Nach dem Tod <a href="http://de.wikipedia.org/wiki/Isabella_I._(Kastilien)" target="_blank">Isabellas 
      von Kastilien</a> (1504) sah sich Kolumbus gezwungen, sich seinem einstigen 
      Gegner Ferdinand von Aragon unterzuordnen. Das Misstrauen indes blieb bestehen, 
      sowohl bei <a href="http://de.wikipedia.org/wiki/Ferdinand_II._(Arag%C3%B3n)" target="_blank">Ferdinand 
      von Aragon</a> als auch bei Christoph Kolumbus. Im Gegensatz zum pragmatischen 
      K&ouml;nig bet&auml;tigte sich der Genueser Seefahrer auf religi&ouml;s-ideologischer 
      Ebene und er blieb dabei keineswegs erfolglos: Die euphorische Beschreibung 
      der leicht zu bekehrenden Indios bei gleichzeitigem Misstrauen gegenüber 
      seinen spanischen Untertanen verfehlte ihre Wirkung bei den Zeitgenossen 
      nicht. Die Brutalit&auml;t der Spanier, von <a href="http://de.wikipedia.org/wiki/Bartolom%C3%A9_de_Las_Casas" target="_blank">Bartolom&eacute; 
      de Las Casas</a>, dem Advokaten der Indios, eindrücklich an den Pranger 
      gestellt, lieferte im 16. und 17. Jahrhundert England und den Niederlanden 
      die (moralische) Rechtfertigung für eine Einmischung in Übersee. 
      <br>
      
    </p>
                       <br>
                         
                    </div>

                    <div class="col-md-6">
                        <p>Unter diesem Gesichtspunkt l&auml;sst sich der Bogen von der ausgestorbenen 
      Anjou-Dynastie zu den aufstrebenden Seenationen der Neuzeit spannen: Das 
      Machtvakuum, das die Erben Kolumbus&#8217; wegen der langen Gefangenschaft 
      des Lu&iacute;s Col&oacute;ns hinterliessen, wurde von den Feinden Habsburgs 
      genutzt. 1586 besetzte Francis Drake die Stadt Santo Domingo und im 17. 
      Jahrhundert liess sich der englische Freibeuter Henry Morgan auf der Markgrafschft 
      Jamaika nieder, einem Besitztum der Familie Col&oacute;n. Von hier aus überfiel 
      er regelm&auml;ssig die aus Südamerika zurückkehrende, spanische 
      Silberflotte.<br>
      Die Vorstellung vom paradiesischen Urzustand, in denen die Indios als &#8222;edle 
      Wilde&#8220; Protagonisten waren, geht auf die von Las Casas redigierte 
      Bordbuchaufzeichnung des Kolumbus zurück. Sie diente den theologischen, 
      philosophischen und juristischen Diskursen der Aufkl&auml;rung. Das &#8222;Verdienst&#8220; 
      des Kolumbus liegt denn auch haupts&auml;chlich in der Schaffung neuer Projektionsfl&auml;chen 
      für Utopien, die in sp&auml;teren Jahrhunderten zum Ventil für 
      unhaltbare Zust&auml;nde in Europa wurden und die die Phantasie auswanderungswilliger 
      Europ&auml;er beflügelten.<br>
      Wenn heute erneut auf die Bilder des Las Casas zurückgegriffen wird 
      (und es wird), so sollte dies im Bewusstsein einer historisch gewachsenen 
      Rezeption geschehen, deren Ursprung nicht zuletzt auf den von mittelalterlichen 
      Vorstellungen beflügelten Kolumbus zurückgeht. Die Schwarz-Weiss-Malerei 
      des M&ouml;nches spiegelt ein Europa des ausgehenden Mittelalters und der 
      frühen Neuzeit, das nicht zuletzt in religi&ouml;sen Fragen zutiefst 
      gespalten gewesen war. Die ursprüngliche Absicht des Las Casas, das 
      Leid der Indios mit neuen radikalen Gesetzen und drastischen Massnahmen 
      zu beseitigen, um sie zu wahren Christen zu machen, ging alsbald vergessen 
      und verkam zu einem willkommenen Propagandainstrument gegen das habsburgische 
      Weltreich. Die radikalen Aussagen von heute haben somit wenig zu tun mit 
      den radikalen Aussagen von früher.</p>
