                    <div class="col-md-12">
                    <h4>Die Ver&ouml;ffentlichung des &#8222;wahren Berichts&#8220; 1571</h4>
                    </div>
                    <div class="col-md-6">
                    
                        <p>Nachdem Lu&iacute;s der Gesellschaft Jesu&#8217; das Erbe 
      seines Onkels Hernando vermacht hatte, erfüllte sich seine Hoffnung 
      auf Freilassung nicht. Er wurde in eine Zelle nach Sevilla verlegt. Lu&iacute;s 
      bemühte sich nun, das letzte ihm verbliebene Pfand seines grossen Erbes, 
      das noch unver&ouml;ffentlichte Manuskript seines Onkels Hernando Col&oacute;n, 
      weiterzureichen. Im Jahr 1560 reisten drei M&auml;nner aus Italien an, denen 
      Lu&iacute;s das Manuskript zusammen mit 2600 Dukaten anvertraute: Baliano 
      di Fornari, ein Genueser Adliger, Pr&oacute;spero Publicola, der sp&auml;tere 
      Kardinal von Santa Cruz, und der spanische Autor, Verleger und Übersetzer 
      Alfonso Ulloa. Die drei reisten von Sevilla nach Venedig weiter, wo Alfonso 
      Ulloa das Manuskript ins Italienische übersetzte (geplant waren auch 
      Publikationen in spanischer und lateinischer Sprache). Doch 1568 geriet 
      der Übersetzer Ulloa in Gefangenschaft. Er begann, verzweifelte Briefe 
      an den spanischen <a href="http://de.wikipedia.org/wiki/Philipp_II._(Spanien)" target="_blank">K&ouml;nig 
      Philipp II.</a> zu schreiben, betonte u. a., dass ihn seine Krankheiten 
      von der Arbeit abhielten und er den n&auml;chsten Winter in seiner kalten 
      Zelle in Venedig nicht mehr überleben werde. Doch Philipp II. hatte 
      keinen Grund, sich für einen Untertanen einzusetzen: Ihm haftete der 
      Makel der pro-franz&ouml;sischen Spionage an (Ulloa war dafür bereits 
      einmal zum Tode verurteilt worden). Ulloa vollendete die Übersetzung, 
      starb aber kurz danach 1570 im Gef&auml;ngnis. Das Original-Manuskript in 
      spanischer Sprache ist seither verschwunden. Dies ist bedauerlich und es 
      ist erstaunlich, dass noch nie danach gesucht wurde. <br>
      Aus einer zuf&auml;llig gefundenen Korrespondenz (1570) zwischen dem Herzog 
      von Alba <a href="http://de.wikipedia.org/wiki/Fernando_%C3%81lvarez_de_Toledo,_Herzog_von_Alba" target="_blank">Fernando 
      &Aacute;lvarez de Toledo</a> und Peter Ernst von Mansfelt geht Interessantes 
      hervor, n&auml;mlich dass Alfonso Ulloa im Gef&auml;ngnis von Venedig zeitgleich 
      an einem kritischen Kommentar über die spanische Besetzung der Niederlande 
      gearbeitet hatte. Der Herzog von Alba schrieb, er werde Ulloa in seiner 
      venezianischen Zelle verh&ouml;ren lassen und alle seine Bücher (todos 
      libros) beschlagnahmen (vgl. die Korrespondenz des Herzogs von Alba mit 
      Peter Ernst von Mansfield, Morel-Fatio 1913). Noch im selben Jahr 1570 starb 
      Ulloa in seiner Zelle. In Zusammenhang mit der Drucklegung eines kleinen 
      Buches (librillo) verweist der Herzog nun auf den oben genannten Kardinal 
      von Santa Cruz (Pr&oacute;spero Publicola) und betont, dass sich Alfonso 
      Ulloa als Sekret&auml;r in dessen Diensten befinde. 1571 wurde Hernandos 
      &#8222;wahrer Bericht&#8220; in Kleinformat (librillo) ediert &#8211; wenige 
      Wochen, bevor Venedig sich der heiligen Liga Philipps II. anschloss und 
      seine Unabh&auml;ngigkeit verlor. Über den Kardinal ist nicht viel 
      bekannt &#8211; ausser, dass er Nuntius am &ouml;sterreichischen und am 
      franz&ouml;sischen Hof gewesen war, bevor er unter dem Konzilspapst Pius 
      IV (1559&#8212;65) eine steile Karriere machte. Falls es dem Herzog von 
      Alba nicht gelungen war, das Manuskript rechtzeitig zu beschlagnahmen und 
      es in die H&auml;nde des Kardinals gelangte, so k&ouml;nnte es sich heute 
      folglich in Rom befinden.<br>
      Die Chronik Hernandos wurde 1571 schliesslich als &#8222;Historie&#8220; 
      bei Franceschi Sanese (Venedig) gedruckt. Die Einleitung schrieb ein gewisser 
      Guiseppe Moleto (1531&#8212;1588), ein süditalienischer Astronom und 
      Hauslehrer des Fürsten Guglielmo Gonzagas III. von Mantua (1538&#8212;1587). 
      Mantua pflegte damals Beziehungen zur Genueser Adelsfamilie Fieschi und 
      zum Herzogtum Montferrat; letzteres war von Margherita Pal&auml;ologa (1510&#8212;1566) 
      einst in ihre Ehe mit dem Fürsten von Mantua eingebracht worden. Ihrem 
      Wappen zufolge war sie eine verarmte Nachkommin der K&ouml;nige von Jerusalem 
      (Re de Gerusaleme) als auch der Imperatori d&#8217; Oriente, der griechischen 
      Dynastie der <a href="http://de.wikipedia.org/wiki/Palaiologen" target="_blank">Pal&auml;ologen</a>. 
      Aus dem Montferrat stammte übrigens auch ein gewisser Baldassare Colombo, 
      der 1572 nach Spanien reiste, um einen Prozess um die Erbfolge des Kolumbus 
      zu initiieren (er gab an, mit dem Entdecker verwandt zu sein). <a href="http://de.wikipedia.org/wiki/Guglielmo_Gonzaga" target="_blank">Guglielmo 
      Gonzaga III.</a> von Mantua unterstützte ihn in dieser Mission nachweislich.
    </p>
